def listafun(lista):
    for i in lista:
        print("Hola!", i)
    print("\n"*2)
    
listafun(["Juan"])
listafun(["Juan", "Carlos"])
listafun(["Juan", "Carlos", "Ana"])
listafun(["Juan", "Carlos", "Ana", "Dylan"])



